package model;

public class CotaModel {
    private int idCota;
    private int numeroCota;
    private int idCliente;

    // Getters e Setters
    public int getIdCota() { return idCota; }
    public void setIdCota(int idCota) { this.idCota = idCota; }
    public int getNumeroCota() { return numeroCota; }
    public void setNumeroCota(int numeroCota) { this.numeroCota = numeroCota; }
    public int getIdCliente() { return idCliente; }
    public void setIdCliente(int idCliente) { this.idCliente = idCliente; }
}